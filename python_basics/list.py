#A list can store any number of items. And these items may be of different types (int, float, str, etc.). For example,


# empty list
list1 = []
print(list1)   # Output: []

# list of integers
numbers = [1, 2, 3]
print(numbers)   # Output: [1, 2, 3]

# list with mixed data types
mixed_list = [1, "Hello", 3.4]
print(mixed_list)   # Output: [1, 'Hello', 3.4]

# list with duplicate items
list2 = [1, 2, 3, 1, 3]
print(list2)   # Output: [1, 2, 3, 1, 3]
